        window.onload = loadCategories;

        /* ── HERO CAROUSEL ───────────────────────────────────────── */
        const carouselContainer = document.querySelector('.carousel-container');
        const slides = document.querySelectorAll('.carousel-slide');
        const dots = document.querySelectorAll('.dot');
        let slideIndex = 0;
        let slideTimer;

        // Check if we show 1 image (phone) or 2 images (laptop)
        function getItemsPerView() {
            return window.innerWidth <= 768 ? 1 : 2;
        }

        function showSlide(index) {
            if (!carouselContainer || slides.length === 0) return;

            const itemsPerView = getItemsPerView();
            const maxIndex = slides.length - itemsPerView;

            // Handle loop wrap-around
            if (index > maxIndex) {
                slideIndex = 0;
            } else if (index < 0) {
                slideIndex = maxIndex;
            } else {
                slideIndex = index;
            }

            // Move the sliding track
            const translateValue = -(slideIndex * (100 / itemsPerView));
            carouselContainer.style.transform = `translateX(${translateValue}%)`;

            // Update navigation dots & hide extra dots on desktop
            dots.forEach((dot, i) => {
                dot.classList.remove('active');
                if (i > maxIndex) {
                    dot.style.display = 'none'; // Hide dead dots on desktop
                } else {
                    dot.style.display = 'inline-block';
                }
            });

            if (dots[slideIndex]) {
                dots[slideIndex].classList.add('active');
            }
        }

        function nextSlide() {
            showSlide(slideIndex + 1);
        }

        function currentSlide(n) {
            clearInterval(slideTimer); 
            showSlide(n);
            startCarousel(); 
        }

        function startCarousel() {
            clearInterval(slideTimer);
            slideTimer = setInterval(nextSlide, 5000); // Auto-slides every 5 seconds
        }

        // Initialize Carousel on page load
        if (slides.length > 0) {
            showSlide(0);
            startCarousel();
            
            // Re-calculate layout automatically if user rotates their phone
            window.addEventListener('resize', () => showSlide(slideIndex));
        }