            btn.addEventListener('click', () => {

                document.querySelectorAll('.material-btn')
                    .forEach(b => b.classList.remove('active'));

                btn.classList.add('active');

                selectedMaterial = btn.dataset.material;

                loadCategories();
                designsWrapper.style.display = 'none';
            });
