        let selectedMaterial = "all";
        /* ── CATALOG LOGIC ────────────────────────────────────── */
        const categoryScroll       = document.getElementById('categoryScroll');
        const designsWrapper       = document.getElementById('designsWrapper');
        const designsScroll        = document.getElementById('designsScroll');
        const currentCategoryTitle = document.getElementById('currentCategoryTitle');

        function loadCategories() {
            categoryScroll.innerHTML = "";

            catalogData.forEach((cat) => {

                // ✅ SIMPLE FILTER
                if (selectedMaterial !== "all" && cat.material !== selectedMaterial) {
                    return;
                }

                const card = document.createElement('div');
                card.className = 'category-card';

                card.onclick = () => showDesigns(cat.id, card);

                card.innerHTML = `
                    <img src="${cat.img}" alt="${cat.name}" class="category-img">
                    <div class="category-info"><h3>${cat.name}</h3></div>
                `;

                categoryScroll.appendChild(card);
            });
        }

        function showDesigns(categoryId, clickedCard) {
            const isAlreadyActive = clickedCard && clickedCard.classList.contains('active');

            document.querySelectorAll('.category-card').forEach(c => c.classList.remove('active'));

            if (isAlreadyActive) {
                designsWrapper.classList.remove('open');
                setTimeout(() => { designsWrapper.style.display = 'none'; }, 350);
                return;
            }

            if (clickedCard) clickedCard.classList.add('active');

            const category = catalogData.find(c => c.id === categoryId);
            currentCategoryTitle.innerText = `${category.name} Collection`;
            designsScroll.innerHTML = '';

            const filteredItems = category.items;

            filteredItems.forEach((item, i) => {
                const itemDiv = document.createElement('div');
                itemDiv.className = 'design-item';
                itemDiv.style.animationDelay = `${i * 0.07}s`;
                itemDiv.innerHTML = `<img src="${item.img}" alt="${item.title}"><h4>${item.title}</h4>`;
                designsScroll.appendChild(itemDiv);
            });

            designsWrapper.style.display = 'block';
            requestAnimationFrame(() => designsWrapper.classList.add('open'));

            if (window.innerWidth < 768) {
                designsWrapper.scrollIntoView({ behavior: 'smooth', block: 'nearest' });
            }
        }

        /* ── SCROLL REVEAL ────────────────────────────────────── */
        const revealObserver = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    entry.target.classList.add('visible');
                    revealObserver.unobserve(entry.target);
                }
            });
        }, { threshold: 0.1, rootMargin: '0px 0px -40px 0px' });

        document.querySelectorAll('.reveal').forEach(el => revealObserver.observe(el));

